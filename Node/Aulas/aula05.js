let numeros = []

for (let num = 1; num <= 10; num++) {
    numeros.push(num)
}
console.log(numeros.reverse()) //inverte a lista
console.log(numeros.shift()) //elimina o primeiro valor da lista
console.log(numeros.sort()) //ordena a lista
console.log(numeros.pop()) //elimina o ultimo valor da lista
console.log(numeros.toString()) //formata a lista em string